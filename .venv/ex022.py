f = input("Digite seu nome completo: ")
print('analisando seu nome...')
print('Seu nome em maiusculo e: {}'.format(f.upper()))
print('Seu nome em minusculo e: {}'.format(f.lower()))
print('Seu nome tem: {} letras'.format(len(f) - f.count(' ')))
print('Seu primeiro nome tem: {} letras'.format(f.find(' ')))







